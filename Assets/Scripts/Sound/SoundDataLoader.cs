public class SoundDataLoader : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////
    // Components
    ///////////////////////////////////////////////////////////////

    public SoundData soundData;

    ///////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////
    // Unity Function
    ///////////////////////////////////////////////////////////////

    private void Start()
    {
        if (SoundManager.Instance != null && soundData != null)
        {
            SoundManager.Instance.LoadSoundData(soundData);
        }
    }
}
